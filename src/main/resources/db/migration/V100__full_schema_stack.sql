-- Цель: убрать оставшиеся двусмысленности на межмодульных стыках,
-- не подменяя доменную логику хрупкими локальными CHECK-ограничениями.
-- =========================================================

-- ---------------------------------------------------------
-- A. Cross-module integrity: Assignment / Testing / Result
-- ---------------------------------------------------------

create or replace function fn_asg_test__integrity()
    returns trigger
    language plpgsql
as $$
declare
    v_course_id bigint;
    v_test_topic_id bigint;
    v_test_type text;
    v_result_attempt_mode text;
    v_result_assignment_id bigint;
    v_result_assignment_test_id bigint;
    v_result_passed boolean;
    v_result_within_deadline boolean;
    v_result_counted_in_assignment boolean;
begin
    if tg_op = 'UPDATE'
        and old.counted_result_id is not null
        and new.counted_result_id is distinct from old.counted_result_id then
        raise exception
            'assignment_test.counted_result_id is write-once once assignment_test is closed by a counted result';
    end if;

    select a.course_id
    into v_course_id
    from assignment a
    where a.id = new.assignment_id;

    if not exists (
        select 1
        from test t
                 join topic tp
                      on tp.id = t.topic_id
        where t.id = new.test_id
          and tp.course_id = v_course_id
    ) then
        raise exception
            'assignment_test.test_id=% does not belong to assignment.course_id=%',
            new.test_id,
            v_course_id;
    end if;

    select t.topic_id, t.test_type
    into v_test_topic_id, v_test_type
    from test t
    where t.id = new.test_id;

    if new.assignment_test_role = 'FINAL_TOPIC_CONTROL'
        and v_test_type <> 'CONTROL' then
        raise exception
            'assignment_test_role=FINAL_TOPIC_CONTROL requires test.test_type=CONTROL';
    end if;

    if new.counted_result_id is not null then
        select
            r.attempt_mode,
            r.assignment_id,
            r.assignment_test_id,
            r.passed,
            r.within_deadline,
            r.counted_in_assignment
        into
            v_result_attempt_mode,
            v_result_assignment_id,
            v_result_assignment_test_id,
            v_result_passed,
            v_result_within_deadline,
            v_result_counted_in_assignment
        from result r
        where r.id = new.counted_result_id;

        if v_result_attempt_mode <> 'ASSIGNED'
            or v_result_assignment_id is distinct from new.assignment_id
            or v_result_assignment_test_id is distinct from new.id
            or v_result_passed is not true
            or v_result_within_deadline is not true
            or v_result_counted_in_assignment is not true then
            raise exception
                'assignment_test.counted_result_id=% must reference a valid counted ASSIGNED result of the same assignment_test',
                new.counted_result_id;
        end if;

        if new.is_closed is not true or new.closed_at is null then
            raise exception
                'assignment_test with counted_result_id must be closed and have closed_at';
        end if;
    end if;

    return new;
end;
$$;

create or replace function fn_test_att__integrity()
    returns trigger
    language plpgsql
as $$
declare
    v_assignment_user_id bigint;
    v_assignment_deadline_at timestamptz;
    v_assignment_test_test_id bigint;
    v_assignment_test_closed boolean;
begin
    if new.attempt_mode = 'ASSIGNED' then
        select
            a.user_id,
            a.deadline_at,
            at.test_id,
            at.is_closed
        into
            v_assignment_user_id,
            v_assignment_deadline_at,
            v_assignment_test_test_id,
            v_assignment_test_closed
        from assignment_test at
                 join assignment a
                      on a.id = at.assignment_id
        where at.id = new.assignment_test_id;

        if not found then
            raise exception
                'ASSIGNED test_attempt requires existing assignment_test_id';
        end if;

        if new.user_id is distinct from v_assignment_user_id then
            raise exception
                'ASSIGNED test_attempt.user_id must match assignment.user_id';
        end if;

        if new.test_id is distinct from v_assignment_test_test_id then
            raise exception
                'ASSIGNED test_attempt.test_id must match assignment_test.test_id';
        end if;

        if new.status in ('STARTED', 'IN_PROGRESS') and v_assignment_test_closed is true then
            raise exception
                'new active ASSIGNED attempt cannot be created or kept active for closed assignment_test_id=%',
                new.assignment_test_id;
        end if;

        if new.started_at > v_assignment_deadline_at then
            raise exception
                'new ASSIGNED attempt cannot start after assignment deadline';
        end if;
    else
        if new.assignment_test_id is not null then
            raise exception
                'SELF test_attempt must not reference assignment_test_id';
        end if;
    end if;

    return new;
end;
$$;

create or replace function fn_result__integrity()
    returns trigger
    language plpgsql
as $$
declare
    v_attempt_mode text;
    v_attempt_assignment_test_id bigint;
    v_attempt_user_id bigint;
    v_attempt_test_id bigint;
    v_assignment_id bigint;
    v_assignment_user_id bigint;
begin
    select
        ta.attempt_mode,
        ta.assignment_test_id,
        ta.user_id,
        ta.test_id
    into
        v_attempt_mode,
        v_attempt_assignment_test_id,
        v_attempt_user_id,
        v_attempt_test_id
    from test_attempt ta
    where ta.id = new.test_attempt_id;

    if not found then
        raise exception
            'result.test_attempt_id=% must reference existing test_attempt',
            new.test_attempt_id;
    end if;

    if new.attempt_mode is distinct from v_attempt_mode then
        raise exception
            'result.attempt_mode must match test_attempt.attempt_mode';
    end if;

    if new.attempt_mode = 'SELF' then
        if v_attempt_assignment_test_id is not null then
            raise exception
                'SELF result cannot be based on test_attempt with assignment_test_id';
        end if;

        if new.assignment_id is not null or new.assignment_test_id is not null then
            raise exception
                'SELF result must not carry assignment_id or assignment_test_id';
        end if;
    else
        if v_attempt_assignment_test_id is distinct from new.assignment_test_id then
            raise exception
                'ASSIGNED result.assignment_test_id must match test_attempt.assignment_test_id';
        end if;

        select
            at.assignment_id,
            a.user_id
        into
            v_assignment_id,
            v_assignment_user_id
        from assignment_test at
                 join assignment a
                      on a.id = at.assignment_id
        where at.id = new.assignment_test_id;

        if not found then
            raise exception
                'ASSIGNED result requires existing assignment_test_id';
        end if;

        if new.assignment_id is distinct from v_assignment_id then
            raise exception
                'ASSIGNED result.assignment_id must match assignment_test.assignment_id';
        end if;

        if v_attempt_test_id is distinct from (
            select at.test_id
            from assignment_test at
            where at.id = new.assignment_test_id
        ) then
            raise exception
                'ASSIGNED result.test_attempt_id must reference attempt for the same assignment_test.test_id';
        end if;

        if v_attempt_user_id is distinct from v_assignment_user_id then
            raise exception
                'ASSIGNED result.test_attempt user must match assignment user';
        end if;
    end if;

    return new;
end;
$$;

create constraint trigger trg_asg_test__integrity
    after insert or update of assignment_id, test_id, assignment_test_role, counted_result_id, closed_at, is_closed
    on assignment_test
    deferrable initially immediate
    for each row
execute function fn_asg_test__integrity();

create constraint trigger trg_test_att__integrity
    after insert or update of user_id, test_id, assignment_test_id, attempt_mode, status, started_at
    on test_attempt
    deferrable initially immediate
    for each row
execute function fn_test_att__integrity();

create constraint trigger trg_result__integrity
    after insert or update of test_attempt_id, attempt_mode, assignment_id, assignment_test_id
    on result
    deferrable initially immediate
    for each row
execute function fn_result__integrity();

